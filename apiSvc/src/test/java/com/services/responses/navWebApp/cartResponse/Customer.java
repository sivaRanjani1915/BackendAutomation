package com.services.responses.navWebApp.cartResponse;

public class Customer {

    public String id;
    public Integer accountID;
    public Integer eGuestID;
    public Double mperksID;
    public String firstName;
    public String lastName;
    public String areaCode;
    public String phoneNumber;
    public String email;
    public Integer teamMemberID;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public Integer getAccountID() {
        return accountID;
    }

    public void setAccountID(Integer accountID) {
        this.accountID = accountID;
    }

    public Integer getEGuestID() {
        return eGuestID;
    }

    public void setEGuestID(Integer eGuestID) {
        this.eGuestID = eGuestID;
    }

    public Double getMperksID() {
        return mperksID;
    }

    public void setMperksID(Double mperksID) {
        this.mperksID = mperksID;
    }

    public String getFirstName() {
        return firstName;
    }

    public void setFirstName(String firstName) {
        this.firstName = firstName;
    }

    public String getLastName() {
        return lastName;
    }

    public void setLastName(String lastName) {
        this.lastName = lastName;
    }

    public String getAreaCode() {
        return areaCode;
    }

    public void setAreaCode(String areaCode) {
        this.areaCode = areaCode;
    }

    public String getPhoneNumber() {
        return phoneNumber;
    }

    public void setPhoneNumber(String phoneNumber) {
        this.phoneNumber = phoneNumber;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public Integer getTeamMemberID() {
        return teamMemberID;
    }

    public void setTeamMemberID(Integer teamMemberID) {
        this.teamMemberID = teamMemberID;
    }

}
